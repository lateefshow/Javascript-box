// JavaScript Unary Operators Example

// --- Unary Operator ---
let a = 5;
console.log("Unary minus (-a):", -a);        // -5 (negation)
console.log("Unary plus (+a):", +a);         // 5 (ensures it's a number)

let isAvailable = false;
console.log("Logical NOT (!isAvailable):", !isAvailable); // true (inverts the boolean)

