// JavaScript Primitive Data Types Example

// String
let name = "Alice";
console.log("Name:", name); // Output: Alice

// Number
let age = 25;
console.log("Age:", age); // Output: 25

// Boolean
let isStudent = true;
console.log("Is Student:", isStudent); // Output: true

// Null
let car = null;
console.log("Car:", car); // Output: null

// Undefined
let city;
console.log("City:", city); // Output: undefined

// Symbol
let id = Symbol("id123");
console.log("Symbol ID:", id); // Output: Symbol(id123)

// BigInt
let bigNumber = 1234567890123456789012345678901234567890n;
console.log("Big Number:", bigNumber); // Output: 1234567890123456789012345678901234567890n
